public class Exponencial{
    public static void main(String [] args){
        int i;
		System.out.print("# ");
		System.out.print("Q ");
		System.out.println("C");        
		for(i = 0;i < 10;i++){
            System.out.print(+i);
            System.out.print("-"+(i*i));
            System.out.println("-"+(i*i*i));
            System.out.println("");
        }   
    }
}


